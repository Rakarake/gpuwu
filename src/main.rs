use winit::{
    event::*,
    event_loop::{ControlFlow, EventLoop},
    window::WindowBuilder,
};

pub async fn run() {
    env_logger::init();
    let event_loop = EventLoop::new().unwrap();
    let window = WindowBuilder::new().build(&event_loop).unwrap();

    // Create render state
    let mut state = State::new(window).await;
    // Event loop
    event_loop.run(move |event, _, control_flow| {
        match event {
                    Event::RedrawRequested(window_id) if window_id == state.window().id() => {
                state.update();
                match state.render() {
                    Ok(_) => {}
                    // Reconfigure the surface if lost
                    Err(wgpu::SurfaceError::Lost) => state.resize(state.size),
                    // The system is out of memory, we should probably quit
                    Err(wgpu::SurfaceError::OutOfMemory) => *control_flow = ControlFlow::Exit,
                    // All other errors (Outdated, Timeout) should be resolved by the next frame
                    Err(e) => eprintln!("{:?}", e),
                }
            },
            //Event::MainEventsCleared => {
            //    // RedrawRequested will only trigger once, unless we manually
            //    // request it.
            //    state.window().request_redraw();
            //},
            Event::WindowEvent {
                ref event,
                window_id,
            } if window_id == state.window().id() => if !state.input(event) { // UPDATED!
                match event {
                    WindowEvent::CloseRequested
                     => *control_flow = ControlFlow::Exit,
                    //| WindowEvent::KeyboardInput {
                    //    input:
                    //        KeyboardInput {
                    //            state: ElementState::Pressed,
                    //            virtual_keycode: Some(VirtualKeyCode::Escape),
                    //            ..
                    //        },
                    //    ..}
                    WindowEvent::KeyboardInput { device_id, event, is_synthetic } => {
                        match event {
                            KeyEvent { physical_key, logical_key, text, location, state: key_state, repeat, .. } => {
                                match physical_key {
                                    winit::keyboard::KeyCode::KeyW => {
                                        println!("BING {:?}", key_state);
                                        state.camera_controller.is_forward_pressed = true;
                                    },
                                    _ => {},
                                }
                            },
                        }
                    },
                    WindowEvent::Resized(physical_size) => {
                        state.resize(*physical_size);
                    }
                    //WindowEvent::ScaleFactorChanged { inner_size_writer, .. } => {
                    //    state.resize(**inner_size_writer);
                    //}
                    _ => {}
                }
            }
            _ => {}
        }
    }).unwrap();
}

use winit::window::Window;

struct State {
    surface: wgpu::Surface,
    device: wgpu::Device,
    queue: wgpu::Queue,
    config: wgpu::SurfaceConfiguration,
    size: winit::dpi::PhysicalSize<u32>,
    window: Window,
    render_pipeline: wgpu::RenderPipeline,
    vertex_buffer: wgpu::Buffer,
    num_vertices: u32,
    index_buffer: wgpu::Buffer,
    num_indices: u32,
    diffuse_bind_group: wgpu::BindGroup,
    diffuse_texture: texture::Texture,
    camera: Camera,
    camera_uniform: CameraUniform,
    camera_buffer: wgpu::Buffer,
    camera_bind_group: wgpu::BindGroup,
    camera_controller: CameraController,
}

#[repr(C)]
#[derive(Copy, Clone, Debug, bytemuck::Pod, bytemuck::Zeroable)]
struct Vertex {
    position: [f32; 3],
    tex_coords: [f32; 2],
}

// NOTE: Macro is used to make this a lot nicer, full control can be achived
// by doing ATTRIBS manually.
impl Vertex {
    const ATTRIBS: [wgpu::VertexAttribute; 2] =
        wgpu::vertex_attr_array![0 => Float32x3, 1 => Float32x2];

    fn desc<'a>() -> wgpu::VertexBufferLayout<'a> {
        use std::mem;

        wgpu::VertexBufferLayout {
            array_stride: mem::size_of::<Self>() as wgpu::BufferAddress,
            step_mode: wgpu::VertexStepMode::Vertex,
            attributes: &Self::ATTRIBS,
        }
    }
}

